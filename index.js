// How to set an item to session storage
sessionStorage.setItem('myKey', 'myValue');

// How to get an item from session storage
const getItem = sessionStorage.getItem('myKey');
console.log(getItem);

// How to remove an item from session storage
sessionStorage.removeItem('myKey'); //after coding this previous value removed 

